<p>Turning Red bercerita tentang Mei Lee (disuarakan oleh Rosalie Chiang) yang merupakan seorang gadis remaja berusia 13 tahun. Mei Lee berkepribadian unik dan memiliki rasa percaya diri yang tinggi.

Mei Lee adalah anak yang berprestasi di sekolahnya, selain itu ia juga memiliki kedekatan dengan keluarganya, yakni Ibunya yang bernama Ming (disuarakan oleh Sandra Oh) dan ayahnya yang bernama Jin (disuarakan oleh Orion Lee).
Ketiganya mengelola kuil penghormatan leluhur mereka, Sun Yee, yang terletak di sebuah pecinan di Toronto, Kanada. Dalam sejarah keluarga mereka, leluhur Sun Yee memiliki kemampuan dan hubungan khusus dengan panda merah.

Dalam film ini, diceritakan Mei Lee menghadapi dilema antara tumbuh menjadi anak baik demi membanggakan ibunya atau bersenang-senang menikmati masa remajanya dengan teman-teman.
